import type { Dimension, Question } from '@/lib/types';

// All 40 quiz questions (spec 2.3). Every question maps to exactly one
// dimension; options are always in score order A=0, B=1, C=2, D=3.
//
// Hebrew: the spec only provides a Hebrew translation for Q001. All other
// `he` strings are empty and MUST be filled by a native copywriter
// (see docs/FOUNDER-QUESTIONS.md B6/B7 — gender-neutral, localized not
// literal). The content-completeness test flags any missing translation.

function q(
  id: string,
  dimension: Dimension,
  text_en: string,
  en: [string, string, string, string],
  text_he = '',
  he: [string, string, string, string] = ['', '', '', ''],
): Question {
  const ids = ['A', 'B', 'C', 'D'] as const;
  return {
    id,
    dimension,
    text_en,
    text_he,
    options: ids.map((oid, i) => ({
      id: oid,
      text_en: en[i],
      text_he: he[i],
      score: i as 0 | 1 | 2 | 3,
    })),
  };
}

